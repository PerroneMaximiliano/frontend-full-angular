@Component({
  selector: 'app-menu',
  templateUrl: './menu.component.html',
  styleUrls: ['./menu.component.css']
})
export class MenuComponent implements OnInit {
  user: User = null;

  constructor(private userService: UserService) { }

  ngOnInit() {
    this.user = this.userService.getIdentity();
  }

}
